package library.MODEL;

public class Student {
	
	private int sID;
	private String sName, sFather, sCourse, sBranch, sYear, sSemester;

	public int getsID() {
		return sID;
	}

	public void setsID(int sID) {
		this.sID = sID;
	}

	public String getsName() {
		return sName;
	}

	public void setsName(String sName) {
		this.sName = sName;
	}

	public String getsFather() {
		return sFather;
	}

	public void setsFather(String sFather) {
		this.sFather = sFather;
	}

	public String getsCourse() {
		return sCourse;
	}

	public void setsCourse(String sCourse) {
		this.sCourse = sCourse;
	}

	public String getsBranch() {
		return sBranch;
	}

	public void setsBranch(String sBranch) {
		this.sBranch = sBranch;
	}

	public String getsYear() {
		return sYear;
	}

	public void setsYear(String sYear) {
		this.sYear = sYear;
	}

	public String getsSemester() {
		return sSemester;
	}

	public void setsSemester(String sSemester) {
		this.sSemester = sSemester;
	}
	
	

}
